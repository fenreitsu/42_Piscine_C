#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
	{
		write (1, str, 1);
		str++;
	}
	write (1, "\n", 1);
}

int	main(int argc, char **argv)
{
	if (argc <= 0)
		return (0);
	else
	{
		while (argv[--argc] && argc > 0)
			ft_putstr(argv[argc]);
	}
}
